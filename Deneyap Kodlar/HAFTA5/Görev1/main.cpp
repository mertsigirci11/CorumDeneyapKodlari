#include <iostream>

using namespace std;

int main()
{
    //For Döngüsü
    cout<<"For Dongusu"<<endl;
    for(int i=1; i<=100; i++)
    {
        cout<<i<<endl;
    }

    //While Döngüsü
    cout<<"While Dongusu"<<endl;
    int sayi=1;
    while(sayi<=100)
    {
        cout<<sayi<<endl;
        sayi++;
    }

    return 0;
}
